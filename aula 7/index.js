//operadores aritiméticos: '+'(adição, concatenação),'-'(subtração),'*'(multiplicação),'/'(divisão)
//'**'(potenciação),'%'(resto da divisão),


//'+'
const num1 = 5;
const num2 = 10;
console.log(num1 + num2);

//'-'
const num3 = 2;
const num4 = 4;
console.log(num3 - num4);

//'*'
const num5 = 6;
const num6 = 2;
console.log(num5 * num6);

//'/'
const num7 = 4;
const num8 = 8;
console.log(num7 / num8);

//'**'
const num9 = 3;
const num10 = 3;
console.log(num9 ** num10);

//'%'
const num11 = 4;
const num12 = 9;
console.log(num11 % num12)

